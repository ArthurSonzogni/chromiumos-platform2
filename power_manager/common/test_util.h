#ifndef POWER_MANAGER_COMMON_TEST_UTIL_H_
#define POWER_MANAGER_COMMON_TEST_UTIL_H_

#include <string>

namespace power_manager {
namespace test {

// Joins a sequence of strings describing actions using commas. The list of
// actions must be terminated by a NULL pointer.
//
// The general pattern is that a test's implementation of a delegate uses
// AppendAction() to build up a string listing actions in the order that they
// are invoked. The test then compares the delegate's list against a string
// generated by passing the expected actions to JoinActions().
std::string JoinActions(const char* action, ...);

// Appends |new_action| to |current_actions|, using a comma as a separator if
// other actions are already listed.
void AppendAction(std::string* current_actions, const std::string& new_action);

}  // namespace test
}  // namespace power_manager

#endif  // POWER_MANAGER_COMMON_TEST_UTIL_H_
